int		ft_count_if(char **tab, int (*f)(char*))
{
	int		j;
	int		flag;

	flag = 0;
	j = 0;
	while (tab[j] != '\0')
	{
		if ((f(tab[j]) == 1))
			flag++;
		j++;
	}
	return (flag);
}
