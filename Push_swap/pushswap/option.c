#include "../commun/push_swap.h"

void		ft_optionv(char **argv, int argc)
{
	int i;

	i = 1;
	while (i < argc - 1)
	{
		argv[i] = argv[i + 1];
		i++;
	}
	argv[i] = "\0";
}
